app.post('/api/chat', async (req, res) => {
  try {
    const { messages } = req.body;

    if (!messages || !Array.isArray(messages)) {
      return res.status(400).json({ error: 'messages が必要です' });
    }

    // Claude API へリクエスト
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-6',
        max_tokens: 2000,
        system: SYSTEM_PROMPT,
        messages: messages
      })
    });

    if (!response.ok) {
      const errorData = await response.json();
      console.error('Claude API エラー:', errorData);
      return res.status(response.status).json({ error: 'API エラーが発生しました' });
    }

    const data = await response.json();
    
    // レスポンスからテキストを抽出
    const assistantMessage = data.content
      .filter(function(block) { return block.type === 'text'; })
      .map(function(block) { return block.text; })
      .join('\n');

    // 会話ログを記録（待たずに送信＝応答を遅らせない）
    const lastUser = messages.slice().reverse().find(function(m) { return m.role === 'user'; });
    const question = lastUser ? (typeof lastUser.content === 'string' ? lastUser.content : JSON.stringify(lastUser.content)) : '';
    logConversation(question, assistantMessage);

    res.json({ message: assistantMessage });

  } catch (error) {
    console.error('サーバーエラー:', error);
    res.status(500).json({ error: 'サーバーエラーが発生しました' });
  }
});

